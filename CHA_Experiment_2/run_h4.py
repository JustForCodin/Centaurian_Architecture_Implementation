#!/usr/bin/env python3
"""
H4 base-capability test runner (Experiment 2 follow-up).

Tests whether the LoRA-10K SCI-grounding adapter causes catastrophic
forgetting on out-of-domain tasks. Compares two conditions on the same
100 probes (h4_probes.json):

  base    - Qwen2.5-7B-Instruct (4-bit NF4), no adapter
  lora    - Qwen2.5-7B-Instruct (4-bit NF4) + LoRA-10K adapter

Probe categories (20 each):
  general_knowledge, code_reasoning, math, instruction_following,
  structured_intent

Each probe is generated by the model under test, then scored 1-5 by
Claude Sonnet 4.5 against a category-specific rubric and the probe's
reference answer.

Outputs (one folder per condition, resumable):
  logs/h4_<condition>/responses.jsonl  - one row per probe, with score

Usage:
  python3 run_h4.py --condition base
  python3 run_h4.py --condition lora --adapter lora_10k

H4 decision rule (§5.3 of plan + §7 of report):
  Pass:   mean(lora) >= mean(base) * 0.95     (< 5% degradation overall)
  Fail:   mean(lora) <  mean(base) * 0.95
The analysis script (analyse_h4.py) computes the verdict per category
and overall, with a paired t-test.
"""

import argparse
import json
import os
import re
import sys
import time
from datetime import datetime, timezone
from pathlib import Path

try:
    from dotenv import load_dotenv
    load_dotenv(Path(__file__).parent / ".env")
except ImportError:
    pass

import torch
import anthropic
from transformers import AutoModelForCausalLM, AutoTokenizer, BitsAndBytesConfig
from peft import PeftModel


# ── Paths ────────────────────────────────────────────────────────────────

BASE_DIR = Path(__file__).parent
PROBES_PATH = BASE_DIR / "h4_probes.json"
ADAPTERS_DIR = BASE_DIR / "adapters"
LOGS_BASE = BASE_DIR / "logs"


# ── Constants ────────────────────────────────────────────────────────────

MODEL_NAME = "Qwen/Qwen2.5-7B-Instruct"
DEFAULT_ADAPTER = "lora_10k"

# Generation config — match Experiment 2 for comparability
GEN_TEMPERATURE = 0.7
GEN_TOP_P = 0.9
GEN_MAX_NEW_TOKENS_DEFAULT = 200

# Per-category generation budgets. Structured intent JSON outputs are
# capped at the spec's max_tokens; the others get the default. Math /
# knowledge answers can be short, but a generous budget lets the model
# show reasoning without being truncated.
CATEGORY_MAX_NEW_TOKENS = {
    "general_knowledge": 120,
    "code_reasoning": 350,
    "math": 200,
    "instruction_following": 250,
    "structured_intent": 120,
}

# Judge — Sonnet 4.5 (same as Exp 1/2 to keep cross-test scores comparable)
JUDGE_PRIMARY_MODEL = "claude-sonnet-4-5"
JUDGE_TEMPERATURE = 0


# ── Condition specs ──────────────────────────────────────────────────────

CONDITIONS = {
    "base": {
        "use_adapter": False,
        "description": "Base Qwen2.5-7B-Instruct (4-bit NF4), no adapter",
    },
    "lora": {
        "use_adapter": True,
        "description": "Qwen2.5-7B-Instruct + LoRA-10K SCI-grounding adapter",
    },
}


# ── System prompts ───────────────────────────────────────────────────────

# Default system prompt: a neutral generic assistant. This is intentionally
# NOT the Aria SCI prompt — H4 measures base capability outside the
# fine-tuned domain.
GENERIC_SYSTEM_PROMPT = "You are a helpful AI assistant. Respond accurately and follow the user's instructions."

# Structured-intent system prompt: the CHA v3 §5.5 transducer role.
# Quoting the paper verbatim so the model is asked to play exactly the
# role it was fine-tuned for, but on intents it has never seen.
STRUCTURED_INTENT_SYSTEM_PROMPT = (
    "You are a natural language surface generator. Convert the structured "
    "intent below into fluent, personality-appropriate speech. Do not add "
    "information beyond the provided knowledge triples. Do not reason or "
    "diagnose. Your output should sound natural and warm, matching the "
    "specified register and emotional valence. Respect the max_tokens "
    "budget and all listed constraints. Output ONLY the surface utterance "
    "(no preface, no JSON echo, no commentary)."
)


def system_prompt_for_probe(probe: dict) -> str:
    if probe["category"] == "structured_intent":
        return STRUCTURED_INTENT_SYSTEM_PROMPT
    return GENERIC_SYSTEM_PROMPT


# ── Model loading (same shape as experiment_runner.py) ───────────────────

_loaded_model = None
_loaded_tokenizer = None


def load_model(use_adapter: bool, adapter_name: str = DEFAULT_ADAPTER):
    global _loaded_model, _loaded_tokenizer
    if _loaded_model is not None and _loaded_tokenizer is not None:
        return _loaded_model, _loaded_tokenizer

    print(f"Loading {MODEL_NAME} in 4-bit NF4...", flush=True)
    bnb = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_compute_dtype=torch.bfloat16,
        bnb_4bit_use_double_quant=True,
    )
    tokenizer = AutoTokenizer.from_pretrained(MODEL_NAME, trust_remote_code=True)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    model = AutoModelForCausalLM.from_pretrained(
        MODEL_NAME,
        quantization_config=bnb,
        device_map="auto",
        trust_remote_code=True,
        torch_dtype=torch.bfloat16,
    )

    if use_adapter:
        adapter_dir = ADAPTERS_DIR / adapter_name
        if not adapter_dir.exists():
            raise FileNotFoundError(
                f"Adapter dir not found: {adapter_dir}. "
                f"Train it first via Cells 8/9/10 of the Colab notebook."
            )
        print(f"Loading LoRA adapter from {adapter_dir}...", flush=True)
        model = PeftModel.from_pretrained(model, str(adapter_dir))

    model.eval()
    _loaded_model = model
    _loaded_tokenizer = tokenizer
    return model, tokenizer


def generate(model, tokenizer, system_prompt: str, user_message: str,
             max_new_tokens: int) -> str:
    messages = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": user_message},
    ]
    prompt = tokenizer.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=True
    )
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            temperature=GEN_TEMPERATURE,
            top_p=GEN_TOP_P,
            do_sample=True,
            pad_token_id=tokenizer.pad_token_id,
        )
    new_tokens = outputs[0][inputs.input_ids.shape[1]:]
    return tokenizer.decode(new_tokens, skip_special_tokens=True).strip()


# ── Judge ────────────────────────────────────────────────────────────────

_anthropic_client = None


def _judge_client() -> anthropic.Anthropic:
    global _anthropic_client
    if _anthropic_client is None:
        key = (
            os.environ.get("CHA_EXPERIMENT_SONNET_KEY")
            or os.environ.get("ANTHROPIC_API_KEY")
        )
        if not key:
            raise RuntimeError(
                "No Anthropic API key (CHA_EXPERIMENT_SONNET_KEY / ANTHROPIC_API_KEY)."
            )
        _anthropic_client = anthropic.Anthropic(api_key=key)
    return _anthropic_client


# Per-category rubric. Each rubric is built around the same 1-5 scale so
# scores aggregate cleanly across categories, but the criteria are tuned
# to what "correct" means for that probe type.
CATEGORY_RUBRICS = {
    "general_knowledge": """Factual-correctness rubric (1-5):
5: Answer is fully correct and matches the reference. Minor extra detail is fine if accurate.
4: Core answer is correct but is incomplete or includes one minor inaccuracy that does not change the main answer.
3: Partially correct — names the right entity/topic but with a factual error (e.g., right person, wrong year), OR hedges into vagueness when a clear answer is expected.
2: Mostly wrong — names a related but incorrect entity, or wrong by a substantial margin (e.g., wrong country, wrong century).
1: Entirely wrong, empty, or gibberish.""",
    "code_reasoning": """Code-correctness rubric (1-5):
5: Code is correct, idiomatic, and clearly addresses the request. Any explanation is accurate.
4: Code is logically correct with minor stylistic issues or trivial off-by-one labels that do not change observable behavior.
3: Code addresses the problem but has a non-trivial bug, misses a stated requirement, or contains a subtle correctness issue on edge cases.
2: Significant logic errors — would not produce the requested behavior on common inputs.
1: Doesn't run, completely wrong, or empty.""",
    "math": """Numerical-correctness rubric (1-5):
5: Final answer is exactly correct (or correct to expected precision). Work, if shown, is right.
4: Final answer is correct but presentation is imprecise (e.g., units missing, slight rounding mismatch) — the numeric content is right.
3: Approach is correct but final answer has a single arithmetic error, OR answer is correct without justification when justification was implied.
2: Substantially wrong final answer; approach may have been on the right track but execution fails.
1: Entirely wrong, empty, or gibberish.""",
    "instruction_following": """Instruction-following rubric (1-5):
5: Fully follows the instruction — every stated constraint (format, length, count, tone) is satisfied and the content is correct.
4: Follows the instruction with one minor deviation (e.g., 4 bullets instead of exactly 3, slight format variance) that doesn't undermine the task.
3: Addresses the topic but misses a constraint or format spec (e.g., produces prose when asked for JSON, or two sentences when asked for one).
2: Mostly ignores the constraints — produces a related output that does not satisfy the stated specification.
1: Doesn't address the task, or is empty.""",
    "structured_intent": """Structured-intent transducer rubric (1-5).
The model received a JSON intent and must produce a natural-language utterance that:
  - matches the register field,
  - satisfies every constraint listed in the constraints array,
  - is grounded in the knowledge_triples (no fabricated facts beyond them),
  - respects the max_tokens budget (treat as a soft cap; +30% is tolerable, more than 2x is a violation),
  - is a surface utterance only (no JSON echo, no meta-commentary).

5: All five requirements satisfied. Output reads naturally.
4: Substantively satisfies the intent, with one minor deviation (e.g., slightly off register, mild over-budget, one constraint partially met).
3: Captures the speech_act and content, but clearly misses one constraint or adds unsupported content from outside the triples.
2: Wrong register OR violates a hard constraint (e.g., gives advice when no_advice is set; diagnoses when no_diagnosis is set; outputs JSON instead of prose).
1: Doesn't fulfill the intent, echoes the JSON, refuses, or is empty.""",
}


JUDGE_SYSTEM_PROMPT_H4 = (
    "You are an expert evaluator. You will be given a probe (a question or "
    "task), an optional reference answer describing what a correct response "
    "looks like, the model's actual response, and a rubric. Score the "
    "response 1-5 using the rubric. Be strict about correctness but fair "
    "about presentation. Reply with ONLY a JSON object: "
    '{"score": N, "reason": "one sentence"}.'
)


def llm_judge(probe: dict, response: str) -> tuple[int, str]:
    """Score one probe response 1-5 against the category rubric."""
    rubric = CATEGORY_RUBRICS[probe["category"]]
    user_prompt = f"""Probe ({probe['category']}):
{probe['prompt']}

Reference / expected answer:
{probe.get('reference', '(none provided — judge on category rubric alone)')}

Model's response:
{response if response.strip() else '(empty)'}

Rubric:
{rubric}

Score the response 1-5 per the rubric. Return ONLY:
{{"score": N, "reason": "one sentence"}}"""

    client = _judge_client()
    last_err = ""
    for attempt in range(5):
        try:
            resp = client.messages.create(
                model=JUDGE_PRIMARY_MODEL,
                max_tokens=150,
                temperature=JUDGE_TEMPERATURE,
                system=JUDGE_SYSTEM_PROMPT_H4,
                messages=[{"role": "user", "content": user_prompt}],
            )
            text = resp.content[0].text.strip()
            if text.startswith("```"):
                text = re.sub(r"^```(?:json)?\s*\n?", "", text)
                text = re.sub(r"\n?```\s*$", "", text)
                text = text.strip()
            try:
                obj = json.loads(text)
            except json.JSONDecodeError:
                m = re.search(r"\{.*\}", text, re.DOTALL)
                if not m:
                    raise
                obj = json.loads(m.group(0))
            return int(obj["score"]), obj.get("reason", "")
        except Exception as e:
            last_err = f"{type(e).__name__}: {e}"
            if attempt < 4:
                time.sleep(2 ** attempt)
    print(f"  Judge failed after 5 attempts: {last_err}", file=sys.stderr)
    return 1, f"judge_error:{last_err[:80]}"


# ── Resume helper ────────────────────────────────────────────────────────

def already_done(logs_dir: Path) -> set[str]:
    path = logs_dir / "responses.jsonl"
    if not path.exists():
        return set()
    done = set()
    with path.open() as f:
        for line in f:
            try:
                rec = json.loads(line)
            except json.JSONDecodeError:
                continue
            if "probe_id" in rec and rec.get("score") is not None:
                done.add(rec["probe_id"])
    return done


# ── Main ─────────────────────────────────────────────────────────────────

def main():
    p = argparse.ArgumentParser(description="CHA H4 base-capability test runner")
    p.add_argument("--condition", required=True, choices=list(CONDITIONS.keys()),
                   help="Which condition to run: base | lora")
    p.add_argument("--adapter", type=str, default=DEFAULT_ADAPTER,
                   help="Adapter folder under adapters/ for --condition lora (default: lora_10k)")
    p.add_argument("--probes", type=str, default=None,
                   help="Optional: comma-separated probe IDs (e.g., 'kn_001,co_005')")
    p.add_argument("--categories", type=str, default=None,
                   help="Optional: comma-separated category names to restrict to")
    args = p.parse_args()

    cond_key = args.condition
    cond = CONDITIONS[cond_key]

    probes_data = json.loads(PROBES_PATH.read_text())
    probes = probes_data["probes"]
    if args.probes:
        wanted = {x.strip() for x in args.probes.split(",")}
        probes = [p for p in probes if p["id"] in wanted]
    if args.categories:
        cats = {x.strip() for x in args.categories.split(",")}
        probes = [p for p in probes if p["category"] in cats]

    logs_dir = LOGS_BASE / f"h4_{cond_key}"
    logs_dir.mkdir(parents=True, exist_ok=True)
    responses_path = logs_dir / "responses.jsonl"

    done = already_done(logs_dir)
    pending = [p for p in probes if p["id"] not in done]

    print("=" * 60)
    print(f"CHA H4 Runner — Condition '{cond_key}'")
    print(f"  {cond['description']}")
    print(f"  Adapter: {args.adapter if cond['use_adapter'] else '(none — base model)'}")
    print(f"  Probes: {len(probes)} total | {len(done)} done | {len(pending)} pending")
    print(f"  Logs: {responses_path}")
    print(f"  Judge: {JUDGE_PRIMARY_MODEL}")
    print("=" * 60)
    if not pending:
        print("All probes already completed for this condition. Nothing to do.")
        return

    model, tokenizer = load_model(cond["use_adapter"], args.adapter)
    print("Model ready.\n", flush=True)

    t_start = time.time()
    with responses_path.open("a") as f_out:
        for i, probe in enumerate(pending, start=1):
            cat = probe["category"]
            max_new = CATEGORY_MAX_NEW_TOKENS.get(cat, GEN_MAX_NEW_TOKENS_DEFAULT)
            system_prompt = system_prompt_for_probe(probe)

            try:
                response = generate(
                    model, tokenizer, system_prompt, probe["prompt"], max_new
                )
            except Exception as e:
                print(f"  [{i}/{len(pending)}] {probe['id']}: gen error: {e}", file=sys.stderr)
                response = ""

            if not response or not response.strip():
                score, reason = 1, "empty_response"
            else:
                score, reason = llm_judge(probe, response)

            record = {
                "probe_id": probe["id"],
                "category": cat,
                "condition": cond_key,
                "prompt": probe["prompt"],
                "reference": probe.get("reference", ""),
                "response": response,
                "score": score,
                "reason": reason,
                "judge_model": JUDGE_PRIMARY_MODEL,
                "timestamp": datetime.now(timezone.utc).isoformat(),
            }
            f_out.write(json.dumps(record) + "\n")
            f_out.flush()

            elapsed = time.time() - t_start
            print(
                f"  [{i:3d}/{len(pending)}] {probe['id']:7s} {cat:22s} "
                f"score={score} | elapsed={elapsed/60:.1f}min",
                flush=True,
            )

    print("\n" + "=" * 60)
    print(f"H4 run complete — condition '{cond_key}'. Wrote {len(pending)} new rows.")
    print(f"  Next: run the other condition, then `python3 analyse_h4.py`")
    print("=" * 60)


if __name__ == "__main__":
    main()
